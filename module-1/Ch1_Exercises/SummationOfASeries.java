/* 
Write a program the displays the result of:
1+2+3+4+5+6+7+8+9
*/

public class SummationOfASeries {
    public static void main(String[] args) { 
        int result;
        result = 1 + 2 + 3 + 4 + 5 + 6 + 7 + 8 + 9;
        System.out.println("The sum of the first 9 integers is " + result);
    }
}